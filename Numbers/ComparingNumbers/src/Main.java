public class Main {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 9;
        System.out.println(num1 == num2);
        System.out.println(num1 > num2);
        System.out.println(num1 < num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 != num2);
    }

}
